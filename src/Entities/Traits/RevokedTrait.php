namespace Shadon\OAuth2\Server\Entities\Traits;

trait RevokedTrait
{
    /**
     * has revoked?
     *
     * @var bool
     */
    protected $revoked = false;

    /**
     * @return bool
     */
    public function isRevoked(): bool
    {
        return $this->revoked;
    }

    /**
     * @param bool $revoked
     */
    public function setRevoked(bool $revoked): void
    {
        $this->revoked = $revoked;
    }
}
